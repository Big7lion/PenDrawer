int main(void)
{

  /* USER CODE BEGIN 1 */
    uint8_t txbf[32]={10,'T','e','m','p',':','4','2','.','3','C'};
    uint8_t rxbf[32]={0};
    uint8_t CMD[32]  = {11,'G','I','V','E','M','E','T','E','M','P','.'};
    uint8_t matchFlag = 1;
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();

  /* USER CODE BEGIN 2 */
    nrf24l01_init();
    OLED_Init();
    OLED_showWord(48,0,0,"System start.");
    OLED_showWord(56,0,0,"%s",&txbf[1]);
    OLED_Clear(0,7,0,127,1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    if(NRF24L01_RxPacket(rxbf) == 0)
    {
        matchFlag = 1;
        for(uint8_t i = 0; i < 12;i++)
        {
            if(rxbf[i] != CMD[i])
            {
                matchFlag = 0;
            }
        }
        if(matchFlag)
        {
            HAL_Delay(30);
            if(NRF24L01_TxPacket(txbf) == TX_OK)
            {
                HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
            }
        }
    }
    /*
        OLED_Clear(0,7,0,127,0);
        OLED_showWord(48,0,0,"%s",rxbf);
        OLED_showWord(56,0,0,"%s",&txbf[1]);
        OLED_Clear(0,7,0,127,1);*/

  }
  /* USER CODE END 3 */

}

